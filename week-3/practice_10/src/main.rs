fn main() {
   let sum = 5550.00 + 7310.00;  // addition
   println!("The sum of 5550 and 7310 = {}",sum);

   let difference:f32 = 95.5 - 4.3;  //subtraction
   println!("The difference of 95.5 and 4.3 = {}",difference);

   let product:f32 = 4.0 * 30.0;  //multiplication
   println!("The multiplicaton of 4 and 30 = {}",product);

   let quotient = 56.7 / 32.2;  //division
   println!("The division of 56.7 and 32.2 = {}",quotient);

   let remainder = 43.0 % 5.0;  //remainder
   println!("The remainder of 43 and 5 = {}",remainder);
}
